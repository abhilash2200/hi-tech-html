    <!-- Page-specific animations -->
    <script>
        (function () {
            'use strict';
            if (typeof gsap === 'undefined' || !window.AnimationUtils) return;

            // Contact section
            const contactSection = document.querySelector('main section');
            if (contactSection) {
                // Heading
                const contactHeading = contactSection.querySelector('h1');
                if (contactHeading) {
                    window.AnimationUtils.fadeInUp(contactHeading, {
                        distance: 30,
                        duration: 0.8,
                        trigger: contactSection,
                        once: true
                    });
                }

                // Contact info list
                const contactList = contactSection.querySelector('ul');
                if (contactList) {
                    const listItems = contactList.querySelectorAll('li');
                    if (listItems.length > 0) {
                        window.AnimationUtils.fadeInUp(listItems, {
                            distance: 20,
                            duration: 0.6,
                            stagger: 0.1,
                            trigger: contactSection,
                            start: 'top 80%',
                            once: true
                        });
                    }
                }

                // Text content
                const contactText = contactSection.querySelector('.text2');
                if (contactText) {
                    window.AnimationUtils.fadeInUp(contactText, {
                        distance: 20,
                        duration: 0.6,
                        trigger: contactSection,
                        start: 'top 75%',
                        once: true
                    });
                }

                // Form
                const contactForm = contactSection.querySelector('form');
                if (contactForm) {
                    window.AnimationUtils.fadeInUp(contactForm, {
                        distance: 30,
                        duration: 0.8,
                        trigger: contactSection,
                        start: 'top 70%',
                        once: true
                    });
                }
            }

            // Refresh ScrollTrigger on resize
            let resizeTimer;
            window.addEventListener('resize', function () {
                clearTimeout(resizeTimer);
                resizeTimer = setTimeout(function () {
                    ScrollTrigger.refresh();
                }, 250);
            });
        })();
    </script>
