            (function () {
                'use strict';
                if (typeof gsap === 'undefined' || !window.AnimationUtils) return;

                // Banner animation
                const banner = document.querySelector('.banner65 h1');
                if (banner) {
                    window.AnimationUtils.fadeInUp(banner, {
                        distance: 30,
                        duration: 0.8,
                        once: true
                    });
                }

                // Blog cards
                const blogSection = document.querySelector('main section');
                if (blogSection) {
                    const blogCards = blogSection.querySelectorAll('.blog_card');
                    if (blogCards.length > 0) {
                        window.AnimationUtils.fadeInUp(blogCards, {
                            distance: 40,
                            duration: 0.8,
                            stagger: 0.1,
                            trigger: blogSection,
                            start: 'top 80%',
                            once: true
                        });
                    }
                }

                // Refresh ScrollTrigger on resize
                let resizeTimer;
                window.addEventListener('resize', function () {
                    clearTimeout(resizeTimer);
                    resizeTimer = setTimeout(function () {
                        ScrollTrigger.refresh();
                    }, 250);
                });
            })();
    </script>
