    <script>
        (function () {
            'use strict';
            if (typeof gsap === 'undefined' || !window.AnimationUtils) return;

            // Banner animation
            const banner = document.querySelector('.banner65 h1');
            if (banner) {
                window.AnimationUtils.fadeInUp(banner, {
                    distance: 30,
                    duration: 0.8,
                    once: true
                });
            }

            // Testimonial cards
            const testimonialSection = document.querySelector('.tetimo section');
            if (testimonialSection) {
                const testimonialCards = testimonialSection.querySelectorAll('.inc-testicard');
                if (testimonialCards.length > 0) {
                    window.AnimationUtils.fadeInUp(testimonialCards, {
                        distance: 40,
                        duration: 0.8,
                        stagger: 0.1,
                        trigger: testimonialSection,
                        start: 'top 80%',
                        once: true
                    });
                }
            }

            // Refresh ScrollTrigger on resize
            let resizeTimer;
            window.addEventListener('resize', function () {
                clearTimeout(resizeTimer);
                resizeTimer = setTimeout(function () {
                    ScrollTrigger.refresh();
                }, 250);
            });
        })();
    </script>
