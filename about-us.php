    <script>
        (function () {
            'use strict';
            if (typeof gsap === 'undefined' || !window.AnimationUtils) return;

            // Banner animation
            const banner = document.querySelector('.banner65 h1');
            if (banner) {
                window.AnimationUtils.fadeInUp(banner, {
                    distance: 30,
                    duration: 0.8,
                    once: true
                });
            }

            // About section content
            const aboutSection = document.querySelector('.about-us-section');
            if (aboutSection) {
                // Badge
                const badge = aboutSection.querySelector('.about-badge');
                if (badge) {
                    window.AnimationUtils.fadeInUp(badge, {
                        distance: 20,
                        duration: 0.6,
                        trigger: aboutSection,
                        start: 'top 80%',
                        once: true
                    });
                }

                // Title
                const title = aboutSection.querySelector('.about-main-title');
                if (title) {
                    window.AnimationUtils.fadeInUp(title, {
                        distance: 30,
                        duration: 0.8,
                        trigger: aboutSection,
                        start: 'top 80%',
                        once: true
                    });
                }

                // Description paragraphs
                const descriptions = aboutSection.querySelectorAll('.about-description');
                if (descriptions.length > 0) {
                    window.AnimationUtils.fadeInUp(descriptions, {
                        distance: 20,
                        duration: 0.6,
                        stagger: 0.1,
                        trigger: aboutSection,
                        start: 'top 75%',
                        once: true
                    });
                }

                // Stats
                const stats = aboutSection.querySelectorAll('.stat-item');
                if (stats.length > 0) {
                    window.AnimationUtils.fadeInUp(stats, {
                        distance: 30,
                        duration: 0.7,
                        stagger: 0.15,
                        trigger: aboutSection,
                        start: 'top 70%',
                        once: true
                    });
                }

                // Image
                const aboutImage = aboutSection.querySelector('.about-main-image');
                if (aboutImage) {
                    window.AnimationUtils.scaleIn(aboutImage, {
                        scale: 1.1,
                        duration: 1,
                        trigger: aboutSection,
                        start: 'top 80%',
                        once: true
                    });
                }

                // Feature cards
                const featureCards = aboutSection.querySelectorAll('.feature-card');
                if (featureCards.length > 0) {
                    window.AnimationUtils.fadeInUp(featureCards, {
                        distance: 40,
                        duration: 0.8,
                        stagger: 0.15,
                        trigger: aboutSection,
                        start: 'top 60%',
                        once: true
                    });
                }

                // Vision & Mission cards
                const vmCards = aboutSection.querySelectorAll('.vision-mission-card');
                if (vmCards.length > 0) {
                    window.AnimationUtils.fadeInUp(vmCards, {
                        distance: 40,
                        duration: 0.8,
                        stagger: 0.2,
                        trigger: aboutSection,
                        start: 'top 50%',
                        once: true
                    });
                }
            }

            // Directors section
            const directorSection = document.querySelector('.director');
            if (directorSection) {
                const heading = directorSection.querySelector('.heading');
                if (heading) {
                    window.AnimationUtils.fadeInUp(heading, {
                        distance: 30,
                        duration: 0.8,
                        trigger: directorSection,
                        once: true
                    });
                }

                const directorCards = directorSection.querySelectorAll('.img-div');
                if (directorCards.length > 0) {
                    window.AnimationUtils.fadeInUp(directorCards, {
                        distance: 40,
                        duration: 0.8,
                        stagger: 0.15,
                        trigger: directorSection,
                        start: 'top 75%',
                        once: true
                    });
                }
            }

            // Refresh ScrollTrigger on resize
            let resizeTimer;
            window.addEventListener('resize', function () {
                clearTimeout(resizeTimer);
                resizeTimer = setTimeout(function () {
                    ScrollTrigger.refresh();
                }, 250);
            });
        })();
    </script>
