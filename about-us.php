        (function () {
            'use strict';
            if (typeof gsap === 'undefined' || !window.AnimationUtils) return;

            // Banner animation
            const banner = document.querySelector('.banner65 h1');
            if (banner) {
                window.AnimationUtils.fadeInUp(banner, {
                    distance: 30,
                    duration: 0.8,
                    once: true
                });
            }

            // About section content
            const aboutSection = document.querySelector('.about-us-section');
            if (aboutSection) {
                // Badge
                const badge = aboutSection.querySelector('.about-badge');
                if (badge) {
                    window.AnimationUtils.fadeInUp(badge, {
                        distance: 20,
                        duration: 0.6,
                        trigger: aboutSection,
                        start: 'top 80%',
                        once: true
                    });
                }

                // Title
                const title = aboutSection.querySelector('.about-main-title');
                if (title) {
                    window.AnimationUtils.fadeInUp(title, {
                        distance: 30,
                        duration: 0.8,
                        trigger: aboutSection,
                        start: 'top 80%',
                        once: true
                    });
                }

                // Description paragraphs
                const descriptions = aboutSection.querySelectorAll('.about-description');
                if (descriptions.length > 0) {
                    window.AnimationUtils.fadeInUp(descriptions, {
                        distance: 20,
                        duration: 0.6,
                        stagger: 0.1,
                        trigger: aboutSection,
                        start: 'top 75%',
                        once: true
                    });
                }

                // Stats
                const stats = aboutSection.querySelectorAll('.stat-item');
                if (stats.length > 0) {
                    window.AnimationUtils.fadeInUp(stats, {
                        distance: 30,
                        duration: 0.7,
                        stagger: 0.15,
                        trigger: aboutSection,
                        start: 'top 70%',
                        once: true
                    });
                }

                // Image
                const aboutImage = aboutSection.querySelector('.about-main-image');
                if (aboutImage) {
                    window.AnimationUtils.scaleIn(aboutImage, {
                        scale: 1.1,
                        duration: 1,
                        trigger: aboutSection,
                        start: 'top 80%',
                        once: true
                    });
                }

                // Feature cards
                const featureCards = aboutSection.querySelectorAll('.feature-card');
                if (featureCards.length > 0) {
                    window.AnimationUtils.fadeInUp(featureCards, {
                        distance: 40,
                        duration: 0.8,
                        stagger: 0.15,
                        trigger: aboutSection,
                        start: 'top 60%',
                        once: true
                    });
                }

                // Vision & Mission cards
                const vmCards = aboutSection.querySelectorAll('.vision-mission-card');
                if (vmCards.length > 0) {
                    window.AnimationUtils.fadeInUp(vmCards, {
                        distance: 40,
                        duration: 0.8,
                        stagger: 0.2,
                        trigger: aboutSection,
                        start: 'top 50%',
                        once: true
                    });
                }
            }

            // Directors section
            const directorSection = document.querySelector('.director');
            if (directorSection) {
                const heading = directorSection.querySelector('.heading');
                if (heading) {
                    window.AnimationUtils.fadeInUp(heading, {
                        distance: 30,
                        duration: 0.8,
                        trigger: directorSection,
                        once: true
                    });
                }

                const directorCards = directorSection.querySelectorAll('.img-div');
                if (directorCards.length > 0) {
                    window.AnimationUtils.fadeInUp(directorCards, {
                        distance: 40,
                        duration: 0.8,
                        stagger: 0.15,
                        trigger: directorSection,
                        start: 'top 75%',
                        once: true
                    });
                }

                const directorCarousel = directorSection.querySelector('.director-carousel');
                if (directorCarousel) {
                    const slides = Array.from(directorCarousel.querySelectorAll('.director-carousel-slide'));
                    const prevBtn = directorCarousel.querySelector('.director-carousel-prev');
                    const nextBtn = directorCarousel.querySelector('.director-carousel-next');
                    const indicators = Array.from(directorCarousel.querySelectorAll('.director-carousel-indicator'));
                    let currentIndex = 0;

                    // Helper to trigger CSS animations on image/text when a slide becomes active.
                    function animateSlideElements(slide) {
                        const imageCol = slide.querySelector('.director-carousel-image-col');
                        const textCol = slide.querySelector('.director-carousel-text-col');

                        if (imageCol) {
                            // Remove and re-add the class so the keyframe animation restarts on every slide change.
                            imageCol.classList.remove('director-slide-animate-image');
                            void imageCol.offsetWidth;
                            imageCol.classList.add('director-slide-animate-image');
                        }

                        if (textCol) {
                            textCol.classList.remove('director-slide-animate-text');
                            void textCol.offsetWidth;
                            textCol.classList.add('director-slide-animate-text');
                        }
                    }

                    function setActiveSlide(newIndex) {
                        if (!slides.length) return;

                        if (newIndex < 0) {
                            newIndex = slides.length - 1;
                        } else if (newIndex >= slides.length) {
                            newIndex = 0;
                        }

                        slides.forEach((slide, index) => {
                            const isActive = index === newIndex;
                            slide.classList.toggle('is-active', isActive);
                            slide.setAttribute('aria-hidden', isActive ? 'false' : 'true');
                        });

                        indicators.forEach((indicator, index) => {
                            const isActive = index === newIndex;
                            indicator.classList.toggle('active', isActive);
                            indicator.setAttribute('aria-selected', isActive ? 'true' : 'false');
                        });

                        currentIndex = newIndex;
                        animateSlideElements(slides[newIndex]);
                    }

                    if (prevBtn) {
                        prevBtn.addEventListener('click', function () {
                            setActiveSlide(currentIndex - 1);
                        });
                    }

                    if (nextBtn) {
                        nextBtn.addEventListener('click', function () {
                            setActiveSlide(currentIndex + 1);
                        });
                    }

                    indicators.forEach((indicator, index) => {
                        indicator.addEventListener('click', function () {
                            setActiveSlide(index);
                        });
                    });

                    // Initialize with first slide active and animated.
                    setActiveSlide(0);
                }
            }

            // Refresh ScrollTrigger on resize
            let resizeTimer;
            window.addEventListener('resize', function () {
                clearTimeout(resizeTimer);
                resizeTimer = setTimeout(function () {
                    ScrollTrigger.refresh();
                }, 250);
            });
        })();
    </script>
