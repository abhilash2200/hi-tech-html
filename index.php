    <script>
        (function () {
            'use strict';

            // Wait for DOM and animation system
            if (typeof gsap === 'undefined' || !window.AnimationUtils) return;

            const shouldDisable = window.AnimationUtils.shouldDisableAnimations();

            // ============================================
            // HERO SECTION - Character Animation
            // ============================================
            (function () {
                const character = document.querySelector('.hero-character');
                const heroSection = document.querySelector('.hero_sec_left');

                if (!character || !heroSection) return;

                if (shouldDisable) {
                    gsap.set(character, { opacity: 1, y: 0, rotation: 0 });
                    return;
                }

                // Entry animation with improved timing
                // Use xPercent and yPercent to maintain centering while animating
                gsap.set(character, { xPercent: -50, yPercent: -50 });

                gsap.fromTo(character,
                    {
                        opacity: 0,
                        y: 40,
                        rotation: -2
                    },
                    {
                        opacity: 1,
                        y: 0,
                        rotation: 0,
                        duration: 1.2,
                        ease: 'power3.out',
                        scrollTrigger: {
                            trigger: heroSection,
                            start: 'top 90%',
                            toggleActions: 'play none none none',
                            once: true
                        },
                        onComplete: function () {
                            // Subtle idle animation (maintains centering)
                            gsap.to(character, {
                                y: -8,
                                rotation: 1.5,
                                duration: 5,
                                ease: 'sine.inOut',
                                yoyo: true,
                                repeat: -1
                            });
                        }
                    }
                );
            })();

            // Character image cycling (only if not disabled)
            if (!shouldDisable) {
                (function () {
                    const characterImages = document.querySelectorAll('.hero-character .character-img');
                    if (characterImages.length === 0) return;

                    let currentIndex = 0;
                    const fadeDuration = 2.5;
                    const displayDuration = 3.5;
                    const cycleDuration = (fadeDuration * 2) + displayDuration;

                    if (characterImages[0]) {
                        gsap.set(characterImages[0], { opacity: 0.15 });
                        characterImages[0].classList.add('active');
                    }

                    function cycleImages() {
                        const currentImg = characterImages[currentIndex];
                        const nextIndex = (currentIndex + 1) % characterImages.length;
                        const nextImg = characterImages[nextIndex];

                        const tl = gsap.timeline({
                            onComplete: function () {
                                currentImg.classList.remove('active');
                                nextImg.classList.add('active');
                            }
                        });

                        tl.to(currentImg, {
                            opacity: 0,
                            duration: fadeDuration,
                            ease: 'power2.inOut'
                        })
                            .fromTo(nextImg,
                                { opacity: 0 },
                                {
                                    opacity: 0.15,
                                    duration: fadeDuration,
                                    ease: 'power2.inOut'
                                },
                                0
                            );

                        currentIndex = nextIndex;
                    }

                    setTimeout(function () {
                        setInterval(cycleImages, cycleDuration * 1000);
                    }, 2500);
                })();
            }

            // ============================================
            // HERO RIGHT - Gallery Items
            // ============================================
            (function () {
                const galleryItems = document.querySelectorAll('.gallery-wrap .item');
                if (galleryItems.length === 0) return;

                window.AnimationUtils.fadeInUp(galleryItems, {
                    distance: 30,
                    duration: 0.8,
                    stagger: 0.1,
                    trigger: document.querySelector('.hero_sec_right'),
                    start: 'top 85%',
                    once: true
                });
            })();

            // ============================================
            // ABOUT US SECTION
            // ============================================
            (function () {
                const aboutSection = document.querySelector('.about_sec');
                if (!aboutSection) return;

                // Section heading
                const aboutHeading = aboutSection.querySelector('.about-heading');
                if (aboutHeading) {
                    window.AnimationUtils.fadeInUp(aboutHeading, {
                        distance: 30,
                        duration: 0.8,
                        trigger: aboutSection,
                        once: true
                    });
                }

                // About text content
                const aboutText = aboutSection.querySelector('.about1-text');
                if (aboutText) {
                    window.AnimationUtils.fadeInUp(aboutText, {
                        distance: 30,
                        duration: 0.8,
                        trigger: aboutSection,
                        start: 'top 80%',
                        once: true
                    });
                }

                // Story section
                const storySection = aboutSection.querySelector('.description');
                if (storySection) {
                    const storyHeading = storySection.querySelector('h1');
                    const storyText = storySection.querySelectorAll('p');
                    const storyImage = storySection.querySelector('img');

                    if (storyHeading) {
                        window.AnimationUtils.fadeInUp(storyHeading, {
                            distance: 20,
                            duration: 0.6,
                            trigger: storySection,
                            once: true
                        });
                    }

                    if (storyText.length > 0) {
                        window.AnimationUtils.fadeInUp(storyText, {
                            distance: 20,
                            duration: 0.6,
                            stagger: 0.1,
                            trigger: storySection,
                            start: 'top 80%',
                            once: true
                        });
                    }

                    if (storyImage) {
                        window.AnimationUtils.scaleIn(storyImage, {
                            scale: 1.05,
                            duration: 0.8,
                            trigger: storySection,
                            start: 'top 80%',
                            once: true
                        });
                    }
                }

                // Opus section
                const opusSection = aboutSection.querySelector('.description2');
                if (opusSection) {
                    const opusHeading = opusSection.querySelector('h1');
                    const opusText = opusSection.querySelectorAll('p');
                    const opusImage = opusSection.querySelector('img');

                    if (opusHeading) {
                        window.AnimationUtils.fadeInUp(opusHeading, {
                            distance: 20,
                            duration: 0.6,
                            trigger: opusSection,
                            once: true
                        });
                    }

                    if (opusText.length > 0) {
                        window.AnimationUtils.fadeInUp(opusText, {
                            distance: 20,
                            duration: 0.6,
                            stagger: 0.1,
                            trigger: opusSection,
                            start: 'top 80%',
                            once: true
                        });
                    }

                    if (opusImage) {
                        window.AnimationUtils.scaleIn(opusImage, {
                            scale: 1.05,
                            duration: 0.8,
                            trigger: opusSection,
                            start: 'top 80%',
                            once: true
                        });
                    }
                }
            })();

            // ============================================
            // DIRECTORS SECTION
            // ============================================
            (function () {
                const directorSection = document.querySelector('.director');
                if (!directorSection) return;

                // Section heading
                const directorHeading = directorSection.querySelector('.heading');
                if (directorHeading) {
                    window.AnimationUtils.fadeInUp(directorHeading, {
                        distance: 30,
                        duration: 0.8,
                        trigger: directorSection,
                        once: true
                    });
                }

                // Director cards with stagger
                const directorCards = directorSection.querySelectorAll('.img-div');
                if (directorCards.length > 0) {
                    window.AnimationUtils.fadeInUp(directorCards, {
                        distance: 40,
                        duration: 0.8,
                        stagger: 0.15,
                        trigger: directorSection,
                        start: 'top 80%',
                        once: true
                    });
                }
            })();

            // ============================================
            // FOOTER SECTION (last-section)
            // ============================================
            (function () {
                const footerSection = document.querySelector('.last-section');
                if (!footerSection) return;

                const footerContent = footerSection.querySelector('.keep');
                const footerForm = footerSection.querySelector('.login-box');

                if (footerContent) {
                    window.AnimationUtils.fadeInUp(footerContent, {
                        distance: 30,
                        duration: 0.8,
                        trigger: footerSection,
                        once: true
                    });
                }

                if (footerForm) {
                    window.AnimationUtils.fadeInUp(footerForm, {
                        distance: 30,
                        duration: 0.8,
                        trigger: footerSection,
                        start: 'top 80%',
                        once: true
                    });
                }
            })();

            // Refresh ScrollTrigger on resize
            let resizeTimer;
            window.addEventListener('resize', function () {
                clearTimeout(resizeTimer);
                resizeTimer = setTimeout(function () {
                    ScrollTrigger.refresh();
                }, 250);
            });

        })();
    </script>
