    <script>
        (function () {
            'use strict';
            if (typeof gsap === 'undefined' || !window.AnimationUtils) return;

            // Course sections - animate main sections
            const mainSections = document.querySelectorAll('.main-div section');
            if (mainSections.length > 0) {
                mainSections.forEach((section) => {
                    const images = section.querySelectorAll('img');
                    const textElements = section.querySelectorAll('.title, .text-div, .number');

                    if (images.length > 0) {
                        window.AnimationUtils.scaleIn(images, {
                            scale: 1.05,
                            duration: 0.8,
                            stagger: 0.1,
                            trigger: section,
                            start: 'top 85%',
                            once: true
                        });
                    }

                    if (textElements.length > 0) {
                        window.AnimationUtils.fadeInUp(textElements, {
                            distance: 30,
                            duration: 0.8,
                            stagger: 0.1,
                            trigger: section,
                            start: 'top 80%',
                            once: true
                        });
                    }
                });
            }

            // Refresh ScrollTrigger on resize
            let resizeTimer;
            window.addEventListener('resize', function () {
                clearTimeout(resizeTimer);
                resizeTimer = setTimeout(function () {
                    ScrollTrigger.refresh();
                }, 250);
            });
        })();
    </script>
